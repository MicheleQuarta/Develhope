package Java;

public class Area_triangolo {
    public static void main(String args[]) {
      int x=10;
      int y=25;
      int z=(x*y)/2;
      
      System.out.println("Base del triangolo: " + x);
      System.out.println("Altezza del triangolo: " + y);
      System.out.println("Area del triangolo: " + z);
    }
}